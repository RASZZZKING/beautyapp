// Helper function to get random element from an array
const getRandomElement = (arr: any[]) =>
  arr[Math.floor(Math.random() * arr.length)];

// Helper function to generate random image URLs
const generateImageUrls = () => {
  const types = [ "sweeater", "hoodie", "trouser", "polo", "polow"];
  const count = Math.floor(Math.random() * 3) + 1;
  return Array.from(
    { length: count },
    () =>
      `/assets/${getRandomElement(types)}${
        Math.floor(Math.random() * 10) + 1
      }.jpeg`
  );
};

// Helper function to generate a random review description
const generateDescription = () => {
  const descriptionsz = descriptions;
  return getRandomElement(descriptionsz);
};

